def download_image(url, filepath):
    if os.path.exists(filepath) and os.path.getsize(filepath) > 1000:
        return True
    try:
        req = urllib.request.Request(url, headers=HEADERS)
        with urllib.request.urlopen(req, context=ctx, timeout=15) as resp:
            data = resp.read()
            if len(data) < 500:
                return False
            with open(filepath, 'wb') as f:
                f.write(data)
            return True
    except:
        return False

def search_amazon_images(query, category="fashion", count=3):
    """Search Amazon and return list of high-res image URLs."""
    encoded = urllib.parse.quote(query)
    url = f"https://www.amazon.com/s?k={encoded}&i={category}"
    req = urllib.request.Request(url, headers=HEADERS)
    try:
        with urllib.request.urlopen(req, context=ctx, timeout=20) as resp:
            html = resp.read().decode('utf-8', errors='ignore')
        pattern = re.compile(r'https://m\.media-amazon\.com/images/I/[A-Za-z0-9_+%-]+\._[^"\']+\.jpg')
        all_imgs = pattern.findall(html)
        seen = set()
        results = []
        for img in all_imgs:
            base = img.split('._')[0]
            if base not in seen and 'icon' not in img.lower() and 'sprite' not in img.lower():
                seen.add(base)
                results.append(base + "._AC_SX679_.jpg")
        return results[:count]
    except:
        return []

def sanitize(name, max_len=80):
    name = re.sub(r'[^\w\s-]', '', name.lower())
    name = re.sub(r'[\s]+', '_', name.strip())
    return name[:max_len]

def crawl_and_save(query, prefix, category="fashion", count=2, delay=1.0):
    """Crawl images for a query and save them. Returns list of filenames."""
    filenames = []
    urls = search_amazon_images(query, category, count + 2)
    for i, url in enumerate(urls[:count]):
        fname = sanitize(f"{prefix}_{query}_{i}") + ".png"
        fpath = str(IMAGES_DIR / fname)
        if download_image(url, fpath):
            filenames.append(fname)
    if delay > 0:
        time.sleep(delay)
    return filenames

