    for i, (query, prefix, category) in enumerate(all_queries):
        print(f"[{i+1}/{total}] Crawling: {query}")
        fnames = crawl_and_save(query, prefix, category, count=2, delay=1.2)
        if fnames:
            downloaded += len(fnames)
            for fn in fnames:
                print(f"  ✅ {fn}")
        else:
            # Check if we already have them
            expected = sanitize(f"{prefix}_{query}_0") + ".png"
            if os.path.exists(str(IMAGES_DIR / expected)):
                skipped += 1
                print(f"  [SKIP] Already exists")
            else:
                failed += 1
                print(f"  ❌ Failed")
    
    print(f"\n📊 Crawl Summary: {downloaded} downloaded, {skipped} skipped, {failed} failed")
    
    # Count images by category
    all_files = [f for f in os.listdir(IMAGES_DIR) if f.endswith('.png')]
    from collections import Counter
    cats = Counter()
    for f in all_files:
        cats[f.split('_')[0]] += 1
    print(f"Total images: {len(all_files)}")
    for c, n in sorted(cats.items()):
        print(f"  {c}: {n}")

